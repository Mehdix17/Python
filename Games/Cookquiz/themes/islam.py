# Arabic text is reversed on the CMD because it does not handle RTL (Right To Left) languages like Arabic
# so i made this function to reverse all strings except numbers in exact questions

def reverse_string(dict):
    reversed_text = dict["text"][::-1]
    reversed_right_answer = dict["right_answer"][::-1]
    reversed_answers = []

    for answer in dict["answers"]:
        reversed_answers.append(answer[::-1])
    
    if dict["points"] == 3: # reverse only the text in exact questions (because right answers are numbers)
        return reversed_text, dict["answers"], dict["right_answer"]
    else:   
        return reversed_text, reversed_answers, reversed_right_answer

